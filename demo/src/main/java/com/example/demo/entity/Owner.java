package com.example.demo.entity;

public class Owner {
	private int ownerID;
	private String ownerName;
	private int ownerPower;
	private String ownerIDCard;
	private String ownerPhone;
	private String ownerWeChat;
	public int getOwnerID() {
		return ownerID;
	}
	public void setOwnerID(int ownerID) {
		this.ownerID = ownerID;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public int getOwnerPower() {
		return ownerPower;
	}
	public void setOwnerPower(int ownerPower) {
		this.ownerPower = ownerPower;
	}
	public String getOwnerIDCard() {
		return ownerIDCard;
	}
	public void setOwnerIDCard(String ownerIDCard) {
		this.ownerIDCard = ownerIDCard;
	}
	public String getOwnerPhone() {
		return ownerPhone;
	}
	public void setOwnerPhone(String ownerPhone) {
		this.ownerPhone = ownerPhone;
	}
	public String getOwnerWeChat() {
		return ownerWeChat;
	}
	public void setOwnerWeChat(String ownerWeChat) {
		this.ownerWeChat = ownerWeChat;
	}
}
